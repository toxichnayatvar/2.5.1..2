import turtle

turtle.shape("turtle")
turtle.fillcolor('#32CD32')
for i in range(4):
    turtle.forward(100)
    turtle.right(90)
    turtle.forward(70)
    turtle.right(90)
    turtle.forward(10)
    turtle.right(90)
    turtle.forward(60)
    turtle.left(90)
    turtle.forward(90)
    turtle.left(90)


turtle.done()